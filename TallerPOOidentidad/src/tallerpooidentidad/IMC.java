    private void jbcrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbcrearActionPerformed
        // TODO add your handling code here:
        
        String nom= jtfnombre.getText();
        int edad=Integer.parseInt(jtfedad.getText());
        char sexo = jtfsexo.getText().charAt(0);
       double peso=Double.parseDouble(jtfpeso.getText()); 
        double altura =Double.parseDouble(jtfaltura.getText());
 
        if(contador==1){
        
            
           per = new Persona(nom,edad,sexo,peso,altura);
            contador++;
            
        }
        if(contador==2){
           
            per=new Persona(nom,edad,sexo);
            per.setpeso(Double.parseDouble(jtfpeso.getText()));
            per.setaltura(Double.parseDouble(jtfaltura.getText()));
            per.setpeso(peso);
            per.setaltura(altura);
            contador++;
       
        }
        if(contador==3){
            
           per=new Persona();
           per.setNombre(nom);
           per.setEdad(edad); 
            per.setpeso(peso);
            per.setaltura(altura); 
           per.setsexo(sexo);
        }
   
        if(per.CalcularIMC()==-1){
         jlpeso.setText("esta por debajo de su peso ideal");
            
        }if(per.CalcularIMC()==0){
            jlpeso.setText("esta en su peso ideal");
            
        }if(per.CalcularIMC()==1){
            
            jlpeso.setText("esta por encima de su peso ideal");
        }
   
        if(per.esMayorDeEdad()){
            jledad.setText("s√≠ es mayor de edad");
        }else{
            
            jledad.setText("no es mayor de edad");
        }
        
       jtainfo.setText(per.toString());
       /*Incompleto
        
        */
        
    }//GEN-LAST:event_jbcrearActionPerformed

